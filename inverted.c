#include<stdio.h>
void main() {

    int i, j;

    for (i = 4; i >= 1; i--) {
        for (j = 1; j <= i; j++) {
            printf("* ");
        }
        printf("\n");
    }

    printf("\n---------------------\n\n");

    for (i = 1; i <= 5; i++) { // 1 2 3 4 5 
        for (j = 5; j >= i; j--) { // 5 4 3 2 1 
            printf("* ");
        }
        printf("\n");
    }

}


